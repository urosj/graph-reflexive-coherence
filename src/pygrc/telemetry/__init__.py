def __getattr__(name: str) -> object:
    if name in _GRCL9_REPLAY_EXPORTS:
        module_name = (
            ".grcl9_lowered_motif_catalog"
            if name
            in {
                "DEFAULT_GRCL9_CATALOG_SOURCE_SESSIONS",
                "GRCL9_REVIEWED_LOWERED_MOTIF_CATALOG_VERSION",
                "GRCL9ReviewedLoweredMotifCatalogSession",
                "run_grcl9_reviewed_lowered_motif_catalog",
            }
            else ".grcl9_replay"
        )
        if name in {
            "GROWTH_SUPERSESSION_SUMMARY_VERSION",
            "GrowthSupersessionSummarySession",
            "run_growth_supersession_summary",
        }:
            module_name = ".grc9_grcl9_growth_supersession_summary"
        if name in {
            "GRCL9_CORRECTED_GROWTH_CATALOG_VERSION",
            "GRCL9CorrectedGrowthCatalogSession",
            "run_grcl9_corrected_growth_catalog",
        }:
            module_name = ".grcl9_corrected_growth_catalog"
        if name in {
            "GROWTH_RECORD_CLASSIFICATION_VERSION",
            "GrowthRecordClassificationSession",
            "run_growth_record_classification",
        }:
            module_name = ".grc9_grcl9_growth_record_classification"
        if name in {
            "GRCL9_SELECTOR_VALIDATION_VERSION",
            "GRCL9SelectorValidationSession",
            "run_grcl9_selector_validation",
        }:
            module_name = ".grcl9_selector_validation"
        if name in {
            "GRCL9V3_REPLAY_ROOT",
            "GRCL9V3_REPLAY_VERSION",
            "run_grcl9v3_lowering_replay_session",
        }:
            module_name = ".grcl9v3_replay"
        if name in {
            "GRCL9V3_SELECTOR_VALIDATION_VERSION",
            "GRCL9V3SelectorValidationSession",
            "run_grcl9v3_selector_validation",
        }:
            module_name = ".grcl9v3_selector_validation"
        if name in {
            "GRCL9V3_REVIEWED_LOWERED_MOTIF_CATALOG_VERSION",
            "GRCL9V3ReviewedLoweredMotifCatalogSession",
            "run_grcl9v3_reviewed_lowered_motif_catalog",
        }:
            module_name = ".grcl9v3_lowered_motif_catalog"
        if name in {
            "GRCL9V3ReplayLaneResult",
            "GRCL9V3ReplaySessionResult",
        }:
            module_name = ".grcl9v3_replay"
        module = import_module(module_name, __name__)
        value = getattr(module, name)
        globals()[name] = value
        return value
    raise AttributeError(f"module {__name__!r} has no attribute {name!r}")

